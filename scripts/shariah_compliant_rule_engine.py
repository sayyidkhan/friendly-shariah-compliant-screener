    def apply_screen(self, df_or_filename, haram_keywords=None, output=0, length=0, unique=False, compression=None, mask=None):
        # Check that 'df_or_filename' is a filepath and try to read
        if isinstance(df_or_filename, str):
            df = _read_data_to_dataframe(df_or_filename)
            if df is None:
                raise Exception("Error encountered with reading the parameter 'df_or_filename' as a filepath into a pandas dataframe.")

        # Check that 'df_or_filename' is a dataframe and try to read
        elif isinstance(df_or_filename, pd.DataFrame):
            df = df_or_filename
        # Final check that 'df_or_filename' is an object that can be converted into a pandas dataframe
        else:
            try:
                df = pd.DataFrame(df_or_filename)
            except:
                raise Exception("Function parameter 'df_or_filename' is incorrect type. It must be a filepath or a pandas dataframe or object readable as a pandas dataframe")

        # Ensure correct data types before calculations
        df['Trailing 24-Month Average Market Capitalization (MkCap)'] = pd.to_numeric(df['Trailing 24-Month Average Market Capitalization (MkCap)'], errors='coerce')
        df['Total Debt (TD)'] = pd.to_numeric(df['Total Debt (TD)'], errors='coerce')
        df['Cash & Short Term Investments (Cash)'] = pd.to_numeric(df['Cash & Short Term Investments (Cash)'], errors='coerce')
        df['Non-Operating Interest Income (Sec)'] = pd.to_numeric(df['Non-Operating Interest Income (Sec)'], errors='coerce')
        df['Accounts Receivables (AR)'] = pd.to_numeric(df['Accounts Receivables (AR)'], errors='coerce')

        # Replace 0 with a very small number to avoid division by zero
        df['Trailing 24-Month Average Market Capitalization (MkCap)'] = df['Trailing 24-Month Average Market Capitalization (MkCap)'].replace(0, 0.000001)

        df['Sum of Cash & Interest-Bearing Securities (Cash+Sec)'] = df['Cash & Short Term Investments (Cash)'].add(df['Non-Operating Interest Income (Sec)'], fill_value=0)
        df['TD/MkCap Ratio'] = df['Total Debt (TD)'] / df['Trailing 24-Month Average Market Capitalization (MkCap)']
        df['Cash+Sec/MkCap Ratio'] = df['Sum of Cash & Interest-Bearing Securities (Cash+Sec)'] / df['Trailing 24-Month Average Market Capitalization (MkCap)']
        df['AR/MkCap Ratio'] = df['Accounts Receivables (AR)'] / df['Trailing 24-Month Average Market Capitalization (MkCap)']
        
        rows = df.shape[0]
        
        if haram_keywords is None:
            words = ['tobacco', 'banking', 'insurance', 'alcohol', 'liquor', 'pork', 'non-halal', 'nonhalal',
                     'entertainment', 'weapon', 'gun', 'rifle', 'missile', 'defense-contractor', 'defense contractor',
                     'hotel', 'nightclub', 'disco', 'night club', 'gambling', 'porn', 'music', 'cinema', 'movie',
                     'cartoon', 'video game', 'video-game', 'computer game']
        else:
            words = haram_keywords

        def check_description(description):
            if not isinstance(description, str):
                return 'Fail'
            for word in words:
                if word in description.lower():
                    return 'Fail'
            return 'Pass'

        df['Description Screening'] = df['Company Description'].apply(check_description)
        
        # Handle NaN values from division before comparison
        df['TD/MkCap < 33%'] = df['TD/MkCap Ratio'].apply(lambda x: 'Pass' if pd.notna(x) and x < 0.333 else 'Fail')
        df['CashSec/MkCap < 33%'] = df['Cash+Sec/MkCap Ratio'].apply(lambda x: 'Pass' if pd.notna(x) and x < 0.333 else 'Fail')
        df['AR/MkCap Ratio < 33%'] = df['AR/MkCap Ratio'].apply(lambda x: 'Pass' if pd.notna(x) and x < 0.333 else 'Fail')

        now_year = datetime.datetime.now().year

        def check_year(year):
            try:
                # Handle cases where year might be a float e.g. '2022.0'
                year_str = str(year).split('.')[0]
                if int(year_str) >= (now_year - 3): # Allow data from last 3 years for more coverage
                    return 'Pass'
            except (ValueError, TypeError):
                pass
            return 'Fail'

        fdy1 = df['Fiscal Data Year (TD)'].apply(check_year)
        fdy2 = df['Fiscal Data Year (Sec)'].apply(check_year)
        fdy3 = df['Fiscal Data Year (Cash)'].apply(check_year)
        fdy4 = df['Fiscal Data Year (AR)'].apply(check_year)

        final_result_mask = (df['Description Screening'] == 'Pass') & \
                            (df['TD/MkCap < 33%'] == 'Pass') & \
                            (df['CashSec/MkCap < 33%'] == 'Pass') & \
                            (df['AR/MkCap Ratio < 33%'] == 'Pass') & \
                            (fdy1 == 'Pass') & (fdy2 == 'Pass') & \
                            (fdy3 == 'Pass') & (fdy4 == 'Pass')
        
        df['Final Result'] = 'Fail'
        df.loc[final_result_mask, 'Final Result'] = 'Pass'

        now = datetime.datetime.now()
        if unique:
            dateversion = now.strftime("%Y-%m-%d-%H.%M.%S")
        else:
            dateversion = now.strftime("%Y-%m-%d")

        if not os.path.exists(str(dateversion)):
            os.mkdir(str(dateversion))

        df_final = df
        filename_suffix = "AllResults"
        if output == 0:
            df_final = df[df['Final Result'] == 'Pass']
            filename_suffix = "PassOnly"
        elif output == 1:
            df_final = df[df['Final Result'] == 'Fail']
            filename_suffix = "FailOnly"

        length_suffix = "FullFeatures"
        if length == 0:
            df_final = df_final.loc[:, ['Ticker', 'Final Result']]
            length_suffix = "MinimumFeatures"
        elif length == 1:
            df_final = df_final.loc[:, ['Ticker','Description Screening','TD/MkCap < 33%','CashSec/MkCap < 33%',
                                        'AR/MkCap Ratio < 33%','Fiscal Data Year (TD)','Fiscal Data Year (Sec)',
                                        'Fiscal Data Year (Cash)','Fiscal Data Year (AR)','Final Result']]
            length_suffix = "MediumFeatures"
            
        filename = os.path.join(str(dateversion), f'{filename_suffix}_{length_suffix}_OutputTable_ScreenedStocks_{dateversion}.csv')

        df_final.to_csv(path_or_buf=filename, mode='w+', compression=compression, index=False)
        return df_final, filename


